#!/usr/bin/env python3

#просто \ - перенос: 2 физических, одна логическая строка
print('Привет! \
      Как дела?') #Привет! Как дела?

#\n перенос на новую строку
print('Привет! \nКак дела?') #Привет!
                             #Как дела?

#\'
print('Hi! It\'s me!') #Hi! It's me!

#\\
print('Path: C:\\Program Files\\Python') # Path: C:\Program Files\Python

#\t - табуляция
print('Привет!\tКак дела?') #Привет! Как дела?

#необрабатываемая строка
print(r'Hi! It\'s me!') #Hi! It\'s me!