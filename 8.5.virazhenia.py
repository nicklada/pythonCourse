#!/usr/bin/env python3
#Длина и ширина прямоугольника хранятся в переменных length и breadth соответственно
length = 5
breadth = 2

#Результат выражения length * breadth сохраняется в переменной area
area = length * breadth

#Передаем значение переменной area в функцию print
print("Площадь прямоугольника равна", area)
#Передаем значение выражения 2 * (length + breadth) в функцию print
print("Периметр прямоугольника равен", 2*(length+breadth))
